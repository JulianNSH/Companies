        <h3>&emsp;Înainte de a face cunoștință cu datele statistice pe țară privind tipurile de întreprinderi din RM, și a ne face un tablou mai clar în ceea ce privește nivelul de rentabilitate a fiecărui tip de întreprindere,
           vă propun ca în cele ce urmează să faceți cunoștință cu câteva particularități privind întreprinderile, antreprenoriatul și activitatea de întreprinzător în general.</h3>
        
         <h4>&emsp;Activitatea de întreprinzător reprezintă un mijloc legal de acumulare a bogățiilor , de asigurare a existenței materiale și spirituale. Această activitate apare odată cu formarea relațiilor de piață adică cu apariția porduselor destinate schimbului de mărfuri.</h4>

        <p><i>&emsp;„Antreprenoriatul este activitatea de fabricare a producției, executare a lucrărilor și prestare a serviciilor, desfășurată de cetățeni și de asociațiile acestora în mod independent, din propria inițiativă, 
        în numele lor, pe riscul propriu și sub răspunderea lor patrimonială cu scopul de a-și asigura o sursă permanentă de venituri. 
        Munca efectuată conform contractului de muncă încheiat, nu este considerată antreprenoriat”</i></p>
        
        <p> <b>&emsp;Persoanele fizice</b> pot desfășura activitatea de întreprinzător prin următoarele forme de organizare juridică: </p>
        &emsp;a)&emsp;În baza patentei de întreprinzător, eliberată de inspectoratul fiscal sau, după caz, de primăria din localitatea în raza căreia se dorește desfășurarea activității;<br>
        &emsp;b)&emsp;În calitate de întreprinzător individual (în legislația mai veche mai figurează și denumirea de întreprindere individuală).
              Întreprinzător individual este persoana fizică, înregistrată în modul stabilit de lege, care desfășoară activitatea de întreprinzător fără a constitui o persoană juridică.
              Înregistrarea întreprinzătorilor individuali se realizează la oficiileteritoriale ale Camerei Înregistrării de Stat. <br>
        &emsp;c)&emsp;Ca gospodărie țărănească(de fermier), este destinată exclusiv desfășurării activității de întreprinzător în domeniul agriculturii.
              Se înregistrează într-un registru special, la primăria din localitatea în care gospodăria țărănească își va desfășura activitatea. 
              La o gospodărie țărănească pot activa atât doar fondatorul acestea cât și membrii ai familiei sau persoane apropiate, deobicei numărul lucrătorilor implicați ajungând până la 9 persoane. <br>
        
       <p>&emsp; <b>Persoanele juridice</b> care desfășoară activitate de întreprinzător se mai numesc persoane juridice cu scop lucrativ (sau cu scop comercial), deoarece boiectivul lor principal constă în acumularea a cât mai multor venituri (câștiguri) Atât în vorbirea curentă cât și în legislație, acestea sunt numite întreprinderi.</p> 
        
       &emsp;În anumite situații, calitate de întreprinzător o poate avea statul și organele administrației publice locale. <br>
       &emsp;În acest caz întreprinderile vor avea titlul de <i> întreprindere de stat</i> și <i>întreprindere municipală</i>. <br>

        <p>&emsp; <u>Pentru că activitaea de întreprinzător a persoanei fizice nu ne oferă un spectrul larg de domenii, trecem mai departe la clasificarea persoanelor juridice cu scop lucrativ.
           În acest pasaj aveți ocazia să cunoașteți despre care sunt cele mai importante forme de întreprinderi și succint cum ele se caracterizează.</u></p>
        
          <p> &emsp; Prin intermediul persoanelor juridice cu scop lucrativ se realizează marea majoritate a activităților de întreprinzător din țară. Acestea iau naștere odată cu înregistrarea lor în Registrul de Stat al persoanelor juridice, care se află la Camera Înregistrării de stat. 
            Ca dovadă a înregistrării unei persoane juridice, statul oferă un certificat de înregistrare care dovedește legalitatea activității persoanei juridice înregistrate.</p>
        
            <p><img src="imagini/img2.jpg" alt="" style="float:right; width: 250px;height: 167px; margin-right:10% "></p>
       <p> &emsp; Așadar, persoanele juridice cu scop lucrativ se împart în felul următor:</p><br>
          &emsp;&emsp; <b>1.	Societăți comerciale:</b> <br>
          &emsp;&emsp;&emsp; a)	SA-societatea pe acțiuni;<br>
          &emsp;&emsp;&emsp; b)	SRL- societate cu răspundere limitată; <br>
          &emsp;&emsp;&emsp; c)	SNC- societate în nume colectiv;<br>
          &emsp;&emsp;&emsp; d)	SC- societate în comandită;<br><br>
          <p><img src="imagini/img4.jpg" alt="" style="float:right; width: 250px;height: 167px; margin-right:10% "></p>
          &emsp;&emsp; <b>2.	Întreprinderile de stat (ÎS) și întreprinderile municipale (ÎM);</b><br><br><br>
        
          &emsp;&emsp; <b> 3.	Cooperativele (Co.):</b><br>
          &emsp;&emsp;&emsp; a)	Cooperativa de producție;<br>
          &emsp;&emsp;&emsp; b)	Cooperativa de întreprinzător;<br><br>

        &emsp;&emsp; <b>4.	Uniuni de persoane juridice (UpJ). </b><br>
       <br><br> <p> &emsp;Mai jos aveți explicate pe larg patru dintre cele mai des întâlnite persoane juridice cu scop lucrativ din RM.</p>
        
        <p> &emsp; <b>Societatea pe acțiuni</b> – este forma cea mai complexă şi, totodată, cea mai evoluată a societăţilor comerciale. 
          În această formă de societate contează mai mult aporturile asociaţilor decât calităţile persoanele ale acestora. 
          În general, asociaţii contribuie cu aporturile lor la formarea capitalului social, fără să desfăşoare o activitate în societate.
           Aceste aporturi prezintă interes şi pentru terţi, deoarece răspunderea asociaţilor pentru obligaţiile sociale se limitează la aceste aporturi.
            Din definiţie rezultă următoarele <b> trăsături specifice ale societăţilor pe acţiuni</b>:</p>
            <p><img src="imagini/img5.jpg" alt="" style="float:right; width: 220px;height: 117px; margin-right:1% "></p><br>
            &emsp; •	societatea pe acţiuni este <b>persoană juridică </b>;<br>
            &emsp; •	societatea pe acţiuni este întotdeauna comercială, precum este şi obiectul său;<br>
            &emsp; •	capitalul social este divizat în acţiuni, care sunt titluri negociabile şi transmisibile;<br>
            &emsp; •	răspunderea asociaţilor pentru obligaţiile sociale este limitată, ei răspunzând numai până la concurenţa capitalului social subscris. <br><br>
            
            <p> &emsp; <b> Societatea cu răspundere limitată </b> – poate fi definită ca o societate constituită, pe baza deplinei încrederi,
             de două sau mai multe persoane care pun în comun anumite bunuri pentru a desfăşura o activitate comercială în vederea împărţirii beneficiilor şi care răspunde pentru obligaţiile sociale.
             Conform art. 145 al Codului civil al Republicii Moldova, societatea cu răspundere limitată este societatea comercială al cărei capital social este divizat în părţi sociale,
             conform actului de constituire şi ale cărei obligaţii sunt garantate cu patrimoniul societăţii. </p>
             <p><img src="imagini/img1.jpg" alt="" style="float:right; width: 284px;height: 190px; margin-right:1% "></p>
             &emsp;Caracterele societăţii cu răspundere limitată: <br>
             &emsp;&emsp;1.	Asocierea intutio personae se bazează pe încrederea asociaţilor ca şi la societăţile de persoane; este o societate relativ închisă;<br>
             &emsp;&emsp;2.	Capitalul social nu poate fi mai mic de 5400 lei şi este divizat în anumite fracţiuni, părţi sociale;<br>
             &emsp;&emsp;3.	Asociaţii răspund pentru obligaţiile sociale, numai în limita aporturilor lor.<br>
             &emsp;&emsp;4.	Numărul asociaţilor poate fi între 2 şi 50; ei participă la deciziile colective ale adunării generale;<br>
             &emsp;&emsp;5.	Societatea dispune de o structură stabilă.<br><br>

             <p> &emsp; <b>Societatea în nume colectiv </b> – este acea societate comercială ale cărei membri practică, în conformitate cu actul de constituire, activitatea de întreprinzător în numele societății și răspund solidar și nelimitat pentru obligațiile acesteia. 
        Asocierea se bazează pe deplina încredere a membrilor asociației. Cel mai frecvent la o asemenea formă recurg rudele și prietenii apropiați, care presupun că se cunosc foarte bine între ei. 
        Numărul asociaților nu poate fi mai mic de 2 și nu poate depăși numărul de 20 de persoane. Capitalul social este divizat în cote-părți, exprimate în bani, iar legislația RM nu prevede un plafon minim pentru acesta.
         Asociații răspund în mod nelimitat (din propriul buzunar) și solidar (împreună) pentru toate datoriile societății. Conducerea întreprinderii se realizează de însăși asociați sau se poate de angajat o persoană din afară care să se ocupe de acest lucru. </p>

         <p> &emsp; <b>Societatea în comandită </b> - este societatea comercială în care, de rând cu membrii care practică în numele societăţii activitate de întreprinzător şi poartă răspundere solidară nelimitată pentru obligaţiile acesteia (comanditaţi),
          există unul sau mai mulţi membri-finanţatori (comanditari) care nu participă la activitatea de întreprinzător a societăţii şi suportă, în limita aportului depus, riscul pierderilor ce rezultă din activitatea acesteia.
           Asocierea se bazează pe încrederea deplină a asociaţilor comanditaţi şi comanditari. Societatea în comandită este o societate intuitu personae; 
          <b> Societatea în comandită </b>este compusă din două categorii de asociaţi, a căror răspundere pentru obligaţiile sociale este diferită: asociaţii comanditaţi răspund nelimitat şi solidar, asociaţii comanditari răspund în limita aportului lor; Capitalul social este divizat în participaţiuni care nu sunt reprezentate prin titluri (certificate). </p>




